char	*ft_strrev(char *str)
{
	int		l;
	int		i;
	char	temp;

	l = 0;
	while (str[l + 1] != '\0')
	{
		l++;
	}
	i = 0;
	while (i < l)
	{
		temp = str[i];
		str[i] = str[l];
		str[l] = temp;
		i++;
		l--;
	}
	return (str);
}
