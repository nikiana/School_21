#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *p;

	p = s1;
	while (*p)
		p++;
	while (n && *s2)
	{
		*p = *s2;
		s2++;
		p++;
		n--;
	}
	while (n != 0)
	{
		*p = '\0';
		n--;
	}
	*p = '\0';
	return (s1);
}
