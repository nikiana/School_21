#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*p;

	i = 0;
	while (src[i])
		i++;
	if (!(p = (char*)malloc(sizeof(char) * i + 1)))
		return (0);
	i = 0;
	while (src[i])
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
