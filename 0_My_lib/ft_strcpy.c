#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*p;
	int		i;

	p = (char*)src;
	i = 0;
	while (p[i])
	{
		dst[i] = p[i];
		i++;
	}
	dst[i] = p[i];
	return (dst);
}
